    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/employee","root","");
            
            Statement st = con.createStatement();
            
            String sql = "select * from emp";
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                
                String Employee_ID= String.valueOf(rs.getInt("Employee_ID"));
                String Name= String.valueOf(rs.getString("Name"));
                String Age= String.valueOf(rs.getInt("Age"));
                String Position= String.valueOf(rs.getString("Position"));
                String Supervisor= String.valueOf(rs.getString("Supervisor"));
                
                String tbData[] = {Employee_ID,Name,Age,Position,Supervisor};
                DefaultTableModel model = (DefaultTableModel)emp_table.getModel();
                
                model.addRow(tbData);
            }
             }catch(Exception e){
                 System.out.println(e.getMessage());
             }
    }//GEN-LAST:event_jButton10ActionPerformed

