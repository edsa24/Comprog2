    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
         DefaultTableModel model = (DefaultTableModel)emp_table.getModel();
       
        String id = txtid.getText();
        String name = txtname.getText();
        String age = txtage.getText();
        String position = txtposition.getText();
        String super1 = txtsuper1.getText();
        
        model.setValueAt(id,emp_table.getSelectedRow(),0);
        model.setValueAt(name,emp_table.getSelectedRow(),1);
        model.setValueAt(age,emp_table.getSelectedRow(),2);
        model.setValueAt(position,emp_table.getSelectedRow(),3);
        model.setValueAt(super1,emp_table.getSelectedRow(),4);
        
        JOptionPane.showMessageDialog(this,"Update Successfully..");
        
        /*}else{*/
            if(emp_table.getRowCount()==0){
                JOptionPane.showMessageDialog(this,"Table is Empty");
            }

    }//GEN-LAST:event_jButton7ActionPerformed

